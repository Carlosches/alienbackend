package icesi.vip.alien.alien.branchAndBound;

public class NodeText {

	private String name;
	private String title;
	
	
	public NodeText(String name, String title) {
		this.name=name;
		this.title=title;
	}


	public String getName() {
		return name;
	}


	public String getTitle() {
		return title;
	}
	
	
	
	
	
}
