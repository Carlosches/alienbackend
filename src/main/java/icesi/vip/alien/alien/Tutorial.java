package icesi.vip.alien.alien;

public class Tutorial {
	
	private final String nombre;
	private final String fecha;
	
	
	public Tutorial(String fecha, String nombre) {
		this.nombre=nombre;
		this.fecha=fecha;
	}


	public String getNombre() {
		return nombre;
	}


	public String getFecha() {
		return fecha;
	}
	
	
	

}
